package com.example.dbproject.noUse;

public class STUDENT {
    public Integer id;
    public String pw;
    public String mame;
    public String division;
    public String department;
    public String penalty_date;
    public String penalty_state;

    public STUDENT() {
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getPw() {
        return pw;
    }

    public void setPw(String pw) {
        this.pw = pw;
    }

    public String getMame() {
        return mame;
    }

    public void setMame(String mame) {
        this.mame = mame;
    }

    public String getDivision() {
        return division;
    }

    public void setDivision(String division) {
        this.division = division;
    }

    public String getDepartment() {
        return department;
    }

    public void setDepartment(String department) {
        this.department = department;
    }

    public String getPenalty_date() {
        return penalty_date;
    }

    public void setPenalty_date(String penalty_date) {
        this.penalty_date = penalty_date;
    }

    public String getPenalty_state() {
        return penalty_state;
    }

    public void setPenalty_state(String penalty_state) {
        this.penalty_state = penalty_state;
    }
}
